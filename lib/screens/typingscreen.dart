  final FirebaseFirestore _firestore = FirebaseFirestore.instance;
  final FirebaseAuth _auth = FirebaseAuth.instance;

  @override
  void initState() {
    super.initState();
    fetchNewText();
  }

  void _startTest(int seconds) {
    setState(() {
      _timeLeft = seconds;
      _isRunning = true;
      _typedChars = 0;
      _correctChars = 0;
      _controller.clear();
      _correctness = List.filled(sampleText.length, false);
    });

    _timer?.cancel();
    _timer = Timer.periodic(Duration(seconds: 1), (timer) {
      setState(() {
        if (_timeLeft > 0) {
          _timeLeft--;
        } else {
          _isRunning = false;
          timer.cancel();
        }
      });
    });
  }

  void _onTextChanged(String text) {
    setState(() {
      _typedChars = text.length;
      _correctChars = 0;
      _correctness = List.filled(sampleText.length, false);
      for (int i = 0; i < text.length && i < sampleText.length; i++) {
        if (text[i] == sampleText[i]) {
          _correctChars++;
          _correctness[i] = true;
        }
      }

      if (_typedChars == sampleText.length) {
        fetchNewText();
      }
    });
  }

  double getWPM() {
    if (_timeLeft == 30) return 0;
    return ((_correctChars / 5) / ((30 - _timeLeft) / 60));
  }

  double getAccuracy() {
    return _typedChars == 0 ? 0 : (_correctChars / _typedChars) * 100;
  }

  void fetchNewText() {
    setState(() {
      _isLoading = true;
      _errorMessage = "";
    });

    try {
      final random = Random();
      final index1 = random.nextInt(localSentences.length);
      final index2 = random.nextInt(localSentences.length);
      final newText = "${localSentences[index1]} ${localSentences[index2]}";

      setState(() {
        sampleText = newText;
        _correctness = List.filled(sampleText.length, false);
        _controller.clear();
        _typedChars = 0;
        _correctChars = 0;
        _isLoading = false;
      });
    } catch (e) {
      setState(() {
        _errorMessage = "An error occurred. Please try again.";
        _isLoading = false;
      });
    }
  }

  Future<void> _uploadWPM() async {
    final user = _auth.currentUser;
    if (user == null) {
      ScaffoldMessenger.of(context).showSnackBar(
        SnackBar(content: Text("You need to be logged in to upload your score.")),
      );
      return;
    }

    final wpm = getWPM();
    if (wpm == 0) {
      ScaffoldMessenger.of(context).showSnackBar(
        SnackBar(content: Text("Your WPM is 0. Please try again.")),
      );
      return;
    }
    Future<String> getUserName(String userId) async {
      final doc = await _firestore.collection('users').doc(userId).get();
      return doc['userName'] ?? "Anonymous";
    }
    Future<void> storeUserName(String userId, String userName) async {
      await _firestore.collection('users').doc(userId).set({
        'userName': userName,
      }, SetOptions(merge: true));
    }

    // Retrieve userName from Firestore
    final userName = await getUserName(user.uid);
    print("Uploading WPM for user: $userName"); // Debugging

    // Use the user's UID as the document ID to overwrite existing data
    await _firestore.collection('wps').doc(user.uid).set({
      'userId': user.uid,
      'userName': userName,
      'wpm': wpm,
      'timestamp': FieldValue.serverTimestamp(),
    }, SetOptions(merge: true)); // Merge to update existing data without overwriting other fields

    ScaffoldMessenger.of(context).showSnackBar(
      SnackBar(content: Text("Your WPM has been uploaded!")),
    );
  }

  Future<void> _showHighScores() async {
    final QuerySnapshot snapshot = await _firestore
        .collection('wps')
        .orderBy('wpm', descending: true)
        .limit(10)
        .get();

    final List<QueryDocumentSnapshot> documents = snapshot.docs;

    // Debugging: Print the retrieved documents
    print("Retrieved ${documents.length} documents:");
    for (var doc in documents) {
      print(doc.data());
    }

    showModalBottomSheet(
      context: context,
      builder: (context) {
        return ListView.builder(
          itemCount: documents.length,
          itemBuilder: (context, index) {
            final data = documents[index].data() as Map<String, dynamic>;
            final userName = data['userName'] ?? "Anonymous"; // Fallback to "Anonymous" if userName is null
            final wpm = data['wpm']?.toStringAsFixed(2) ?? "0.00"; // Fallback to "0.00" if wpm is null

            return ListTile(
              leading: Text("${index + 1}", style: GoogleFonts.orbitron(
                fontSize: 20,
                color: Color(0xFFFF6B00),
              )),
              title: Text(userName, style: GoogleFonts.orbitron(
                fontSize: 20,
                color: Color(0xFFFF6B00),
              )),
              trailing: Text("$wpm WPM", style: GoogleFonts.orbitron(
                fontSize: 20,
                color: Theme.of(context).colorScheme.inversePrimary,
              )),
            );
          },
        );
      },
    );
  }

